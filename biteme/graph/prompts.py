REVIEWER = """\
你是一位严格的评审员。根据以下三段内容：
- 问题
- 用户回答
- LLM 参考答案

【步骤 1 — 提取关键词】
根据问题和参考答案，总结该题考察的 1–3 个核心知识点关键词。
关键词应是可以被单独学习的具体技术概念（算法名称、数学原理、架构组件、核心特性）。
例如：「缩放点积注意力」「B+ 树索引」「Copy-on-Write」「梯度消失」。
不得使用描述提问方式的词汇（如「机制类比」「原因分析」）或过于宽泛的词汇（如「机制理解」「设计思想」）。

【步骤 2 — 独立评分】
对比用户回答与 LLM 参考答案，对每个关键词给出 0–10 的整数分。
评分综合考量准确性（与参考答案一致）、完整性（要点是否遗漏）、深度（是否触及核心原理），三者同等重要。
- 10 分：与参考答案高度一致且有深度
- 5–6 分：覆盖核心要点但有明显遗漏或表述不够准确
- 0 分：与参考答案严重偏差或完全未提及
若用户回答为空或与问题完全无关，所有关键词得 0 分。

只输出合法 JSON，不得包含 Markdown 代码围栏（```）或任何其他文字：
{"keywords": [{"keyword": "...", "score": 0}]}
"""


MEMORY_MERGE = """\
你是用户学习记忆的管理员。你将收到：
1. 旧记忆（JSON 数组，可能为空）
2. 本次 session 的 review_history（JSON 数组的数组，每轮为一个对象列表，例如：[[{"keyword":"梯度消失","score":7}],[{"keyword":"向量检索","score":4}]]）

任务：
- 将 review_history 中的关键词与旧记忆中的关键词进行语义比对，含义相近的视为同一条目，选定一个规范名称
- 同一条目：将本次所有相关轮次的得分（整数）**逐条**追加到旧 scores 列表末尾
- 新出现的关键词：创建新条目，scores 为本次各轮得分列表
- 旧记忆中未被本次覆盖的条目：原样保留（只保留 keyword 和 scores 字段）
- 不要计算或输出平均分，平均分由调用方计算

只输出合法 JSON 数组，不含 Markdown 代码围栏或任何其他文字。
格式：[{"keyword": "...", "scores": [...]}]
"""


def get_prompts(mode: str) -> dict[str, str]:
    if mode == "learn":
        return {
            "questioner": LEARN_QUESTIONER,
            "answerer": LEARN_ANSWERER,
            "planner": LEARN_PLANNER,
            "reviewer": REVIEWER,
        }
    return {
        "questioner": INTERVIEW_QUESTIONER,
        "answerer": INTERVIEW_ANSWERER,
        "planner": INTERVIEW_PLANNER,
        "reviewer": REVIEWER,
    }
